package group.guangdong.pojo;

import java.util.Date;

public class Flink {
    private Integer flinkId;

    private String flinkName;

    private String flinkAddress;

    private String flinkUploader;

    private Date flinkUploadTime;

    private Integer flinkShow;

    public Integer getFlinkId() {
        return flinkId;
    }

    public void setFlinkId(Integer flinkId) {
        this.flinkId = flinkId;
    }

    public String getFlinkName() {
        return flinkName;
    }

    public void setFlinkName(String flinkName) {
        this.flinkName = flinkName;
    }

    public String getFlinkAddress() {
        return flinkAddress;
    }

    public void setFlinkAddress(String flinkAddress) {
        this.flinkAddress = flinkAddress;
    }

    public String getFlinkUploader() {
        return flinkUploader;
    }

    public void setFlinkUploader(String flinkUploader) {
        this.flinkUploader = flinkUploader;
    }

    public Date getFlinkUploadTime() {
        return flinkUploadTime;
    }

    public void setFlinkUploadTime(Date flinkUploadTime) {
        this.flinkUploadTime = flinkUploadTime;
    }

    public Integer getFlinkShow() {
        return flinkShow;
    }

    public void setFlinkShow(Integer flinkShow) {
        this.flinkShow = flinkShow;
    }
}